    public void listarTabela(){
        
        DefaultTableModel modelo = (DefaultTableModel)tabCadProf.getModel();
        modelo.setRowCount(0);
        int posLin = 0;
        
        for(Sala sal1 :gerSal.getBdSal()){
            modelo.insertRow(posLin, new Object[]{sal1.getSnome(),sal1.getCapacidade(),sal1.getPresenca(),sal1.getEndereco()});
            posLin++;
        }
    }
    
    public void limpar(){
        txtSnome.setText("");
        txtCapacidade.setText("");
        txtPresenca.setText("");
        txtEndereço.setText("");
        txtSnome.requestFocus();
    }
    
    public void consultar(){
        sal = new Sala();
        
        sal.setSnome(Integer.parseInt(txtSnome.getText()));
        
        sal = gerSal.consSalSnome(sal);
        
        if(sal == null){
            JOptionPane.showMessageDialog(null, "Não existe uma sala cadastrada com esse código: "+txtSnome.getText(), "Erro de consulta", 0);
            limpar();
        }
        else{
            txtSnome.setText(Integer.toString(sal.getSnome()));
            txtCapacidade.setText(Integer.toString(sal.getCapacidade()));
            txtPresenca.setText(sal.getPresenca());
            txtEndereço.setText(sal.getEndereco());
        }
    }
    
